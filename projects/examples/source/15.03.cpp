///////////////////////////////////////////////////////////////////////////////////////////////

#include <algorithm>
#include <array>
#include <iterator>

///////////////////////////////////////////////////////////////////////////////////////////////

#include <boost/asio.hpp>

///////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    boost::asio::io_context context;

//  -------------------------------------------------------------------------------------------

    boost::asio::ip::tcp::endpoint endpoint(boost::asio::ip::make_address("127.0.0.1"), 1'024);

//  -------------------------------------------------------------------------------------------

    boost::asio::ip::tcp::socket socket(context);

//  -------------------------------------------------------------------------------------------

    socket.connect(endpoint);

//  -------------------------------------------------------------------------------------------

    std::array < char, 1 << 10 > array = {};

//  -------------------------------------------------------------------------------------------

    std::ranges::fill(array, 'a');

//  -------------------------------------------------------------------------------------------

    auto buffer = boost::asio::buffer(array, std::size(array));

//  -------------------------------------------------------------------------------------------

    boost::asio::write(socket, buffer);

    boost::asio::read (socket, buffer);

//  -------------------------------------------------------------------------------------------

    auto lambda = [](auto x){ assert(x == 'a'); };

//  -------------------------------------------------------------------------------------------

    std::ranges::for_each(array, lambda);
}

