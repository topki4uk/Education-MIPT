////////////////////////////////////////////////////////////////////////////////

#include <algorithm>
#include <array>
#include <bit>
#include <cassert>
#include <cstring>
#include <iterator>

////////////////////////////////////////////////////////////////////////////////

#include <arpa/inet.h>
#include <netinet/in.h>
#include <sys/socket.h>
#include <unistd.h>

////////////////////////////////////////////////////////////////////////////////

int main()
{
    auto descriptor = socket(AF_INET, SOCK_STREAM, 0);

//  ----------------------------------------------------------------------------

    sockaddr_in server;

//  ----------------------------------------------------------------------------

    std::memset(&server, 0, sizeof(sockaddr_in));

//  ----------------------------------------------------------------------------

    server.sin_family = AF_INET;

//  ----------------------------------------------------------------------------

    inet_pton(AF_INET, "127.0.0.1", &server.sin_addr);

//  ----------------------------------------------------------------------------

    server.sin_port = htons(443);

//  ----------------------------------------------------------------------------

    connect(descriptor, std::bit_cast < sockaddr * > (&server), sizeof(server));

//  ----------------------------------------------------------------------------

    std::array < char, 1 << 10 > array = {};

//  ----------------------------------------------------------------------------

    std::ranges::fill(array, 'a');

//  ----------------------------------------------------------------------------

    auto begin = std::begin(array), end = std::end(array);

//  ----------------------------------------------------------------------------

    while (begin != end)
    {
        auto size = send(descriptor, begin, std::distance(begin, end), 0);

    //  ------------------------------------------------------------------

        std::advance(begin, size);
    }

//  ----------------------------------------------------------------------------

    begin = std::begin(array);

//  ----------------------------------------------------------------------------

    while (begin != end)
    {
        auto size = recv(descriptor, begin, std::distance(begin, end), 0);

    //  ------------------------------------------------------------------

        std::advance(begin, size);
    }

//  ----------------------------------------------------------------------------

    auto lambda = [](auto x){ assert(x == 'a'); };

//  ----------------------------------------------------------------------------

    std::ranges::for_each(array, lambda);

//  ----------------------------------------------------------------------------

    close(descriptor);
}

