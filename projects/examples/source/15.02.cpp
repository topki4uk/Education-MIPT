///////////////////////////////////////////////////////////////////////////////

#include <array>
#include <iterator>

///////////////////////////////////////////////////////////////////////////////

#include <boost/asio.hpp>

///////////////////////////////////////////////////////////////////////////////

int main()
{
    boost::asio::io_context context;

//  ---------------------------------------------------------------------------

    boost::asio::ip::tcp::endpoint endpoint(boost::asio::ip::tcp::v4(), 1'024);

//  ---------------------------------------------------------------------------

    boost::asio::ip::tcp::acceptor acceptor(context, endpoint, true);

//  ---------------------------------------------------------------------------

    boost::asio::ip::tcp::socket socket(context);

//  ---------------------------------------------------------------------------

    acceptor.accept(socket);

//  ---------------------------------------------------------------------------

    std::array < char, 1 << 10 > array = {};

//  ---------------------------------------------------------------------------

    auto buffer = boost::asio::buffer(array, std::size(array));

//  ---------------------------------------------------------------------------

    boost::asio::read (socket, buffer);

    boost::asio::write(socket, buffer);
}

