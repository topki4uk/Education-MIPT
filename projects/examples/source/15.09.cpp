#include <algorithm>
#include <cassert>
#include <cstring>
#include <iterator>
#include <vector>

///////////////////////////////////////////////////////////////////////////////////

#include <arpa/inet.h>
#include <netinet/in.h>
#include <sys/socket.h>
#include <unistd.h>

///////////////////////////////////////////////////////////////////////////////////

int main()
{
    auto descriptor = socket(AF_INET, SOCK_STREAM, 0);

//  -------------------------------------------------------------------------------

    sockaddr_in server;

//  -------------------------------------------------------------------------------

    std::memset(&server, 0, sizeof(sockaddr_in));

//  -------------------------------------------------------------------------------

    server.sin_family = AF_INET;

//  -------------------------------------------------------------------------------

    inet_pton(AF_INET, "127.0.0.1", &server.sin_addr);

//  -------------------------------------------------------------------------------

    server.sin_port = htons(80);

//  -------------------------------------------------------------------------------

    connect(descriptor, reinterpret_cast < sockaddr * > (&server), sizeof(server));

//  -------------------------------------------------------------------------------

    std::vector < char > vector(1 << 10, 'a');

//  -------------------------------------------------------------------------------

    auto begin = &*std::begin(vector), end = &*std::end(vector);

//  -------------------------------------------------------------------------------

    while (begin != end)
    {
        std::advance(begin, send(descriptor, begin, std::distance(begin, end), 0));
    }

//  -------------------------------------------------------------------------------

    begin = &*std::begin(vector);

//  -------------------------------------------------------------------------------

    while (begin != end)
    {
        std::advance(begin, recv(descriptor, begin, std::distance(begin, end), 0));
    }

//  -------------------------------------------------------------------------------

    auto lambda = [](auto x){ assert(x == 'a'); };

//  -------------------------------------------------------------------------------

    std::ranges::for_each(vector, lambda);

//  -------------------------------------------------------------------------------

    close(descriptor);
}

