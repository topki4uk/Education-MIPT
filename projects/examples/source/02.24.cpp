#include <iostream>

////////////////////////////////////////////////////////////

int main()
{
	double eps;
	double summa = 1, term = 1.0;
	std::cin >> eps;

	int i = 1;
	while (term >= eps)
	{
		term /= i;
		summa += term;
		i++;
	}
	
	std::cout << summa << '\n';
}

