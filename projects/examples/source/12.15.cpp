#include <cassert>
#include <regex>
#include <string>

/////////////////////////////////////////////////////

auto replace(std::string const & string)
{
    std::regex regex(R"(a{5})");

    auto format = "bbbbb";

    return std::regex_replace(string, regex, format);
}

/////////////////////////////////////////////////////

int main()
{
    assert(replace("aaaaa") == "bbbbb");
}

