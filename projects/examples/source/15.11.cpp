#include <algorithm>
#include <cassert>
#include <cstddef>
#include <vector>

///////////////////////////////////////////////////////////////////////////

#include <boost/asio.hpp>

///////////////////////////////////////////////////////////////////////////

int main()
{
    boost::asio::io_context context;

//  -----------------------------------------------------------------------

    boost::asio::ip::tcp::socket socket(context);

//  -----------------------------------------------------------------------

    auto address = boost::asio::ip::make_address("127.0.0.1");

//  -----------------------------------------------------------------------

    boost::asio::ip::tcp::endpoint endpoint(address, 80);

//  -----------------------------------------------------------------------

    socket.connect(endpoint);

//  -----------------------------------------------------------------------

    std::vector < char > vector(1 << 10, 'a');

//  -----------------------------------------------------------------------

    boost::asio::write(socket, boost::asio::buffer(vector));

//  -----------------------------------------------------------------------

    auto lambda = [&vector](boost::system::error_code const &, std::size_t)
    {
        auto lambda = [](auto x){ assert(x == 'a'); };

        std::ranges::for_each(vector, lambda);
    };

//  -----------------------------------------------------------------------

    boost::asio::async_read(socket, boost::asio::buffer(vector), lambda);

//  -----------------------------------------------------------------------

    context.run();
}

